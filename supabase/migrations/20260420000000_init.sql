-- ─────────────────────────
-- users
-- ─────────────────────────

-- 所有人可以讀公開用戶資料（未被軟刪除）
create policy "users: public read"
  on public.users for select
  using (deleted_at is null);

-- 只有本人可以更新自己的資料
create policy "users: self update"
  on public.users for update
  using (auth.uid() = id);


-- ─────────────────────────
-- artist_profiles
-- ─────────────────────────

create policy "artist_profiles: public read"
  on public.artist_profiles for select
  using (true);

create policy "artist_profiles: self insert"
  on public.artist_profiles for insert
  with check (auth.uid() = user_id);

create policy "artist_profiles: self update"
  on public.artist_profiles for update
  using (auth.uid() = user_id);

create policy "artist_profiles: self delete"
  on public.artist_profiles for delete
  using (auth.uid() = user_id);


-- ─────────────────────────
-- artworks
-- 發佈的作品全公開；草稿 / 封存只有藝術家自己看得到
-- ─────────────────────────

create policy "artworks: read"
  on public.artworks for select
  using (
    (status = 'published' and deleted_at is null)
    or exists (
      select 1 from public.artist_profiles ap
      where ap.id = artworks.artist_id
        and ap.user_id = auth.uid()
    )
  );

create policy "artworks: artist insert"
  on public.artworks for insert
  with check (
    exists (
      select 1 from public.artist_profiles ap
      where ap.id = artist_id
        and ap.user_id = auth.uid()
    )
  );

create policy "artworks: artist update"
  on public.artworks for update
  using (
    exists (
      select 1 from public.artist_profiles ap
      where ap.id = artworks.artist_id
        and ap.user_id = auth.uid()
    )
  );

create policy "artworks: artist delete"
  on public.artworks for delete
  using (
    exists (
      select 1 from public.artist_profiles ap
      where ap.id = artworks.artist_id
        and ap.user_id = auth.uid()
    )
  );


