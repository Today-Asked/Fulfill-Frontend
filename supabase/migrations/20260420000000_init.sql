-- =========================
-- ENUM TYPES
-- =========================

create type artwork_status as enum ('draft','published','archived');
create type media_type as enum ('image','video');
create type message_type as enum ('text','image','file','commission','sticker');
create type commission_status as enum ('pending','accepted','rejected','in_progress','delivered','completed');


-- =========================
-- USERS
-- id 直接對應 auth.users.id（uuid），由 trigger 自動建立
-- =========================

create table public.users (
    id         uuid primary key references auth.users(id) on delete cascade,
    email      varchar(255) unique,
    username   varchar(50) unique,
    name       varchar(100),
    birthday   timestamptz,
    avatar_url varchar(500),
    bio        text,
    created_at timestamptz default now(),
    updated_at timestamptz default now(),
    deleted_at timestamptz
);

-- Trigger：用戶一完成 Supabase Auth 註冊，自動新增一筆到 public.users
create or replace function public.handle_new_user()
returns trigger as $$
begin
  insert into public.users (id, email)
  values (new.id, new.email);
  return new;
end;
$$ language plpgsql security definer;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();


-- =========================
-- ARTIST PROFILES
