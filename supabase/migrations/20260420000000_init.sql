-- =========================
-- CONVERSATIONS
-- 注意：應用層必須保證 usera_id < userb_id，避免重複建立
-- =========================

create table public.conversations (
    id              bigint generated by default as identity primary key,
    usera_id        uuid references public.users(id),
    userb_id        uuid references public.users(id),
    last_message_at timestamptz,
    created_at      timestamptz default now(),
    unique (usera_id, userb_id)
);


-- =========================
-- MESSAGES
-- =========================

create table public.messages (
    id         bigint generated by default as identity primary key,
    chat_id    bigint references public.conversations(id) on delete cascade,
    sender_id  uuid references public.users(id),
    type       message_type,
    content    jsonb,
    read_at    timestamptz,
    created_at timestamptz default now(),
    deleted_at timestamptz   -- 軟刪除，不真刪
);


-- =========================
-- COMMISSION REQUESTS
-- =========================

create table public.commission_requests (
    id             bigint generated by default as identity primary key,
    chat_id        bigint references public.conversations(id),
    client_id      uuid references public.users(id),
