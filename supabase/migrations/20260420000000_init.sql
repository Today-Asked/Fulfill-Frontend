-- ─────────────────────────
-- conversations
-- ─────────────────────────

create policy "conversations: participant read"
  on public.conversations for select
  using (auth.uid() = usera_id or auth.uid() = userb_id);

create policy "conversations: authenticated insert"
  on public.conversations for insert
  with check (auth.uid() = usera_id or auth.uid() = userb_id);

create policy "conversations: participant update"
  on public.conversations for update
  using (auth.uid() = usera_id or auth.uid() = userb_id);


-- ─────────────────────────
-- messages
-- ─────────────────────────

-- 對話參與者才能看訊息（排除軟刪除）
create policy "messages: participant read"
  on public.messages for select
  using (
    deleted_at is null
    and exists (
      select 1 from public.conversations c
      where c.id = messages.chat_id
        and (c.usera_id = auth.uid() or c.userb_id = auth.uid())
    )
  );

-- 只有對話參與者才能發訊息，且 sender_id 必須是自己
create policy "messages: sender insert"
  on public.messages for insert
  with check (
    auth.uid() = sender_id
    and exists (
      select 1 from public.conversations c
      where c.id = chat_id
        and (c.usera_id = auth.uid() or c.userb_id = auth.uid())
    )
  );

-- 只能更新自己的訊息（用於軟刪除 deleted_at）
create policy "messages: sender update"
  on public.messages for update
  using (auth.uid() = sender_id);


